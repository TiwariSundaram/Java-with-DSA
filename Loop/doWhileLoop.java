import java.util.*;

public class doWhileLoop {

    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);

        System.out.println("Enter the num :");
        int a = sc.nextInt();

        int count = 0;

        do { 
            System.out.println("jay shree Ram");
            count++;
        } while (count<a);

    }
    
}
