public class typeCasting {
  
    public static void main(String args[]){

        // float to int
        float a = 74.4f;
        int b = (int)a;

        System.out.println(b);

        // double to float
        double c = 42547.35;
        float d = (float)c;

        System.out.println(d);
    }
}
